package main

import (
	"errors"
	"fmt"
)

func main() {
	fmt.Printf("this is go hello")

}

// hello 用于生成问候内容。
func hello(name string) (string, error) {
	if name == "" {
		return "", errors.New("empty name")
	}
	return fmt.Sprintf("Hello, %s!", name), nil
}

